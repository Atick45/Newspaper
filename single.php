								<p class="card-text">1979 সালে রোহিঙ্গাদের শরণার্থীদের প্রথম প্রত্যাবাসন তিনটি কারণে সম্ভব ছিল। প্রথমত, বাংলাদেশ আন্তর্জাতিক সঙ্কটের সঙ্কটকে বর্জন না করার সিদ্ধান্ত নিয়েছে, তবে দ্বিপাক্ষিক পর্যায়ের এ বিষয়ে হস্তক্ষেপ করেছে। দ্বিতীয়ত, মিয়ানমার সরকার সকল উদ্বাস্তুকে গ্রহণ করার জন্যই নয়, বরং তাদের নিরাপত্তার নিশ্চয়তা প্রদান করে এই সমস্যার সমাধান করতে ইতিবাচক প্রতিক্রিয়া ব্যক্ত করেছে। তৃতীয়ত, রোহিঙ্গাদের গোপন রাজনৈতিক সংগঠন ফেরত পাঠায় না। </p>
