								<p class="card-text">ফ্যাসিস্ট নাগরিকত্ব বিষয়টি তাদের ধর্ম (মুসলিম) এবং তাদের ভাষা (বাঙ্গালী থেকে প্রাপ্ত একটি উপভাষা) দ্বারা আরও জটিল। এই রোহিঙ্গারা আরাকানের সাথে মিশ্রিত করতে বাধা দেয়, যারা ধর্মের দ্বারা প্রধানত বৌদ্ধ। এই পার্থক্যটিও মিয়ানমারের (তারপর বার্মা) ব্রিটিশদের কাছ থেকে রোহিঙ্গাদের কিছুটা রাজনৈতিক উচ্চাকাঙ্খী করে তোলে। মিয়ানমার আরাকান রাজ্যে চীনা ও থাই সীমান্তের সাথে গেরিলা গোষ্ঠীকে লড়াই করার সময় রোহিঙ্গারা তাদের নিজস্ব রাষ্ট্র প্রতিষ্ঠার জন্য গোপন রাজনৈতিক দল গঠন করে। </p>
